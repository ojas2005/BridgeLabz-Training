using System;
class AgeOfHarry{
    static void Main()
    {
        int birthYear = 2000; //Year of birth
        int currentYear = 2024; //Current year
        int age = currentYear-birthYear; //Calculating the age
        Console.WriteLine($"Harry's age in 2024 is {age}"); //Printing the age. Output:- Harry's age in 2024 is 24.
    }
}